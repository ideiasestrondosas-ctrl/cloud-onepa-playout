echo -e "üì° Stream:   http://localhost:3000/hls/stream.m3u8"
echo "--------------------------------------------------"
echo "üîë Login padr√£o: admin / admin"
echo "‚ö†Ô∏è  Nota: Altere a senha ap√≥s o primeiro login."
echo ""
